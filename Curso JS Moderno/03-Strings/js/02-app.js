console.log(producto.length);// length nos da la cantidad de caracteres del string.



console.log(producto.indexOf('Monitor'));// indexOf nos dice en qué posición se encuentra el texto que le pasamos como argumento. Si no lo encuentra, devuelve -1.


console.log(producto.includes('Monitor'));// includes nos dice si el texto que le pasamos como argumento está incluido en el string. Devuelve true o false.
