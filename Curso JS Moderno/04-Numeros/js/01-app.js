


const number1 = 20;// Número.
const number2 = 20.2;// Número decimal.
const number3 = .102030;// Número decimal con punto.
//No hace falta el uso de comillas para definir un número. Ni especificsar el tipo de dato.
const number4 = -20;// Incluso se pueden usar números negativos.
// Los números pueden ser enteros o decimales, positivos o negativos. Pero todos se crean igual, sin comillas.

console.log(number1);
console.log(number2);
console.log(number3);
console.log(number4);

const number5 = "20";// Número mediante string.

console.log(number5); // Esto es un string, no un número.